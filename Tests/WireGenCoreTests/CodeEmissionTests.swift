import Testing

@testable import WireGenCore

@Suite("CodeEmission")
struct CodeEmissionTests {
    // MARK: - Helpers

    private func singleton(
        _ name: String,
        dependencies: [(name: String?, type: String)] = []
    ) -> DiscoveredSingleton {
        let deps = dependencies.map {
            DependencyParameter(name: $0.name, type: $0.type, kind: .injectInitParameter)
        }
        return DiscoveredSingleton(
            typeName: name,
            typeKind: "struct",
            genericParameterNames: [],
            dependencies: deps,
            sourcePath: "\(name).swift"
        )
    }

    // MARK: - Header

    @Test func outputBeginsWithDoNotEditBanner() {
        let output = renderWireGraph(topologicalOrder: [])
        #expect(output.hasPrefix("// Generated by WireGen — do not edit."))
    }

    @Test func declaresInternalSendableStruct() {
        let output = renderWireGraph(topologicalOrder: [singleton("A")])
        #expect(output.contains("internal struct _WireGraph: Sendable {"))
    }

    // MARK: - Empty graph

    @Test func emptyGraphEmitsBootstrapWithNoBindings() {
        let output = renderWireGraph(topologicalOrder: [])
        // Empty graph still produces a valid struct so consumers can call
        // `_WireGraph.bootstrap()` unconditionally.
        #expect(output.contains("static func bootstrap() async throws -> _WireGraph {"))
        #expect(output.contains("_WireGraph()"))
        // No stored properties → no `let x: T` lines.
        #expect(!output.contains("    let "))
    }

    // MARK: - Stored properties

    @Test func singleSingletonProducesOneStoredProperty() {
        let output = renderWireGraph(topologicalOrder: [singleton("A")])
        #expect(output.contains("    let a: A"))
    }

    @Test func multipleSingletonsProduceOneStoredPropertyEach() {
        let output = renderWireGraph(topologicalOrder: [
            singleton("A"),
            singleton("B"),
            singleton("C"),
        ])
        #expect(output.contains("    let a: A"))
        #expect(output.contains("    let b: B"))
        #expect(output.contains("    let c: C"))
    }

    @Test func storedPropertyNamesAreLowerCamelCased() {
        let output = renderWireGraph(topologicalOrder: [
            singleton("TaskRepository"),
            singleton("DynamoDBTaskRepository"),
        ])
        #expect(output.contains("    let taskRepository: TaskRepository"))
        // Naive lowercasing keeps acronyms intact rather than reflowing
        // them — `DynamoDBTaskRepository` → `dynamoDBTaskRepository`.
        #expect(output.contains("    let dynamoDBTaskRepository: DynamoDBTaskRepository"))
    }

    // MARK: - Bootstrap construction order

    @Test func bootstrapConstructsBindingsInTopologicalOrder() {
        // C is constructed before B before A so that arguments are in scope
        // when each constructor runs.
        let output = renderWireGraph(topologicalOrder: [
            singleton("C"),
            singleton("B", dependencies: [(name: "c", type: "C")]),
            singleton("A", dependencies: [(name: "b", type: "B")]),
        ])
        let cIndex = output.range(of: "let c = C(")!.lowerBound
        let bIndex = output.range(of: "let b = B(c: c)")!.lowerBound
        let aIndex = output.range(of: "let a = A(b: b)")!.lowerBound
        #expect(cIndex < bIndex)
        #expect(bIndex < aIndex)
    }

    @Test func bootstrapNoDependenciesEmitsBareInitCall() {
        let output = renderWireGraph(topologicalOrder: [singleton("A")])
        #expect(output.contains("let a = A()"))
    }

    @Test func bootstrapMultipleDependenciesProducesArgumentList() {
        let output = renderWireGraph(topologicalOrder: [
            singleton("First"),
            singleton("Second"),
            singleton(
                "Pair",
                dependencies: [(name: "first", type: "First"), (name: "second", type: "Second")]
            ),
        ])
        #expect(output.contains("let pair = Pair(first: first, second: second)"))
    }

    // MARK: - Wildcard label handling

    @Test func wildcardDependencyLabelOmittedFromCallSite() {
        // `init(_ a: A)` — the call site must omit the label, so the
        // generated bootstrap emits `X(a)` not `X(_: a)`.
        let output = renderWireGraph(topologicalOrder: [
            singleton("A"),
            singleton("X", dependencies: [(name: nil, type: "A")]),
        ])
        #expect(output.contains("let x = X(a)"))
        #expect(!output.contains("X(_: "))
    }

    @Test func mixedLabeledAndWildcardArgumentsRenderCorrectly() {
        let output = renderWireGraph(topologicalOrder: [
            singleton("A"),
            singleton("B"),
            singleton(
                "X",
                dependencies: [(name: nil, type: "A"), (name: "second", type: "B")]
            ),
        ])
        #expect(output.contains("let x = X(a, second: b)"))
    }

    // MARK: - Final return

    @Test func bootstrapReturnsMemberwiseInitWithAllProperties() {
        let output = renderWireGraph(topologicalOrder: [
            singleton("A"),
            singleton("B"),
        ])
        // Memberwise init takes one argument per stored property in
        // declaration order.
        #expect(output.contains("return _WireGraph(a: a, b: b)"))
    }

    @Test func bootstrapSignatureIsAsyncThrows() {
        // `async throws` is locked in regardless of current bindings —
        // M6 lifecycle work needs the option without a breaking-change
        // ripple through every consumer's call site.
        let output = renderWireGraph(topologicalOrder: [singleton("A")])
        #expect(output.contains("static func bootstrap() async throws -> _WireGraph {"))
    }
}

import Foundation

/// Render the `_WireGraph.swift` source the build plugin writes alongside
/// the consumer target. The generated struct has one stored property per
/// binding (named by lowerCamelCased type name) and a static
/// `bootstrap() async throws -> _WireGraph` that constructs each binding
/// in dependency order.
///
/// `bootstrap` is declared `async throws` regardless of whether the
/// current set of `@Singleton` types actually have async/throws inits.
/// M6's lifecycle work makes async/throws constructor injection a
/// first-class option, and locking the signature in now means later
/// migrations don't ripple through every consumer's `try await
/// _WireGraph.bootstrap()` call site. Swift accepts an `async throws`
/// function body that doesn't actually use those capabilities.
package func renderWireGraph(topologicalOrder: [DiscoveredSingleton]) -> String {
    var lines: [String] = []

    lines.append("// Generated by WireGen — do not edit.")
    lines.append("")
    lines.append("internal struct _WireGraph: Sendable {")

    guard !topologicalOrder.isEmpty else {
        // Empty graph — no bindings to construct, but still emit a
        // valid struct so consumers can call `_WireGraph.bootstrap()`
        // without conditional compilation.
        lines.append("    static func bootstrap() async throws -> _WireGraph {")
        lines.append("        _WireGraph()")
        lines.append("    }")
        lines.append("}")
        lines.append("")
        return lines.joined(separator: "\n")
    }

    // One stored property per binding.
    for singleton in topologicalOrder {
        let propertyName = lowerCamelCased(singleton.typeName)
        lines.append("    let \(propertyName): \(singleton.typeName)")
    }

    lines.append("")
    lines.append("    static func bootstrap() async throws -> _WireGraph {")

    // Construction body — each binding is constructed in dependency
    // order, so any value referenced as an argument is already in scope
    // as a previously-bound local.
    for singleton in topologicalOrder {
        let propertyName = lowerCamelCased(singleton.typeName)
        let arguments = singleton.dependencies.map(renderArgument).joined(separator: ", ")
        lines.append("        let \(propertyName) = \(singleton.typeName)(\(arguments))")
    }

    // Final return — the memberwise init takes one argument per stored
    // property, in declaration order.
    let initArguments = topologicalOrder.map { singleton -> String in
        let name = lowerCamelCased(singleton.typeName)
        return "\(name): \(name)"
    }.joined(separator: ", ")
    lines.append("        return _WireGraph(\(initArguments))")

    lines.append("    }")
    lines.append("}")
    lines.append("")

    return lines.joined(separator: "\n")
}

/// Render a single `(label: value)` or bare-`value` argument, depending
/// on whether the parameter has an external label or is wildcard.
private func renderArgument(for dependency: DependencyParameter) -> String {
    let value = lowerCamelCased(dependency.type)
    if let label = dependency.name {
        return "\(label): \(value)"
    }
    // Wildcard external label — the call site omits it entirely.
    return value
}

/// Convert a type name to a property name by lowercasing the first
/// character. `B` → `b`, `TaskRepository` → `taskRepository`,
/// `DynamoDBTaskRepository` → `dynamoDBTaskRepository`.
///
/// The simplest convention that produces unambiguous, distinct names
/// within a graph (the strict-on-ambiguity rule guarantees no two types
/// share a name). More sophisticated acronym-aware casing
/// (`URLSession` → `urlSession`) can be added later if generated names
/// look awkward enough to bother with.
private func lowerCamelCased(_ typeName: String) -> String {
    guard let first = typeName.first else { return typeName }
    return first.lowercased() + typeName.dropFirst()
}
